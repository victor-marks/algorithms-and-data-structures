/*
Write a function called naiveSearch which accepts a string and a substring and returns how many times the substring occurs in the first string.
Searching - O(n^2)
*/

function naiveSearch(long, short) {
  let count = 0;

  for (let i = 0; i < long.length; i++) {
    for (let j = 0; j < short.length; j++) {
      if (short[j] !== long[i + j]) break;
      if (j === short.length - 1) count++;
    }
  }
  return count;
}

naiveSearch('lorie loled', 'lol');
